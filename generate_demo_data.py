SENTIMENTS = ["positive", "negative", "neutral"]


def generate_note_id(title, time_str, salt=""):
    raw = f"{title}_{time_str}_{salt}"
    return hashlib.md5(raw.encode()).hexdigest()[:16]


def generate_variant_note(platform, category, base_time, templates, keywords_map):
    """基于模板生成变体笔记"""
    if not templates:
        return None

    base = random.choice(templates)
    now = datetime.now()

    # 60%概率使用原始标题, 40%生成变体
    if random.random() < 0.6:
        title = base["title"]
    else:
        # 生成标题变体
        keyword_group = random.choice(base.get("keywords", ["支付"]))
        sub_keywords = keywords_map.get(category, {}).get(keyword_group, [keyword_group])
        kw = random.choice(sub_keywords) if sub_keywords else keyword_group
        prefixes = [
            f"{kw}体验分享", f"关于{kw}的真实感受",
            f"{kw}最新消息", f"聊聊{kw}那些事",
            f"{kw}使用攻略", f"{kw}深度测评",
            f"{kw}值不值得用？", f"我的{kw}经历",
        ]
        title = random.choice(prefixes)

    desc = base["desc"]
    sentiment = base["sentiment"] if random.random() > 0.3 else random.choice(SENTIMENTS)

    # 时间偏移
    time_offset = timedelta(days=random.randint(-3, 3), hours=random.randint(0, 23), minutes=random.randint(0, 59))
    note_time = base_time + time_offset
    if note_time > now:
        note_time = now - timedelta(hours=random.randint(1, 48))

    time_str = note_time.strftime('%Y-%m-%d %H:%M')
    note_id = generate_note_id(title + str(random.randint(0, 999999)), time_str, platform)

    # 互动数据
    days_ago = (now - note_time).days
    if days_ago < 7:
        liked = random.randint(5, 800)
    elif days_ago < 30:
        liked = random.randint(20, 3000)
    elif days_ago < 180:
        liked = random.randint(50, 8000)
    else:
        liked = random.randint(100, 15000)

    comments = random.randint(1, max(2, liked // 4))
    collected = random.randint(1, max(2, liked // 3))

    # 关键词标签
    kw_tags = base.get("keywords", [])
    sub_kws = []
    for kw_group in kw_tags:
        subs = keywords_map.get(category, {}).get(kw_group, [])
        if subs:
            sub_kws.extend(random.sample(subs, min(2, len(subs))))

    categories = [category]
    if random.random() < 0.15:
        other_cats = [c for c in PLATFORMS[platform]["categories"] if c != category]
        if other_cats:
            categories.append(random.choice(other_cats))

    # 留痕机制：部分笔记标记为已删除（模拟用户删帖）
    is_deleted = False
    deleted_at = ""
    snapshot_time = time_str
    # 越早的帖子越有可能被删除；负面帖子删除概率更高
    delete_prob = 0.03  # 基础3%概率
    if sentiment == "negative":
        delete_prob = 0.08  # 负面帖子8%概率被删
    if days_ago > 180:
        delete_prob += 0.03  # 半年前的帖子额外3%概率被删
    if random.random() < delete_prob:
        is_deleted = True
        # 删除时间在发布后1-60天内
        delete_delay = timedelta(days=random.randint(1, 60), hours=random.randint(0, 23))
        deleted_time = note_time + delete_delay
        if deleted_time > now:
            deleted_time = now - timedelta(hours=random.randint(1, 24))
        deleted_at = deleted_time.strftime('%Y-%m-%d %H:%M')

    return {
        "note_id": note_id,
        "platform": platform,
        "title": title,
        "desc": desc,
        "author": random.choice(NICKNAMES),
        "author_avatar": "",
        "author_id": f"user_{random.randint(100000, 999999)}",
        "liked_count": str(liked),
        "comment_count": str(comments),
        "collected_count": str(collected),
        "cover": "",
        "link": f"https://www.xiaohongshu.com/explore/{note_id}",
        "time": time_str,
        "categories": categories,
        "keyword_tags": sub_kws,
        "search_keyword": f"{platform}{category}",
        "xsec_token": "",
        "sentiment": sentiment,
        "is_deleted": is_deleted,
        "deleted_at": deleted_at,
        "snapshot_time": snapshot_time,
    }


